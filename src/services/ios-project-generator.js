export function generateIosProjectBlueprint(spec) {
  const projectName = spec.export.xcodeProjectName;

  return {
    projectName,
    directories: [
      `${projectName}/App`,
      `${projectName}/Core`,
      `${projectName}/Features`,
      `${projectName}/Integrations`,
      `${projectName}/Resources`
    ],
    files: {
      [`${projectName}/App/${projectName}App.swift`]: swiftAppEntry(spec),
      [`${projectName}/Core/AppSpec.json`]: JSON.stringify(spec, null, 2),
      [`${projectName}/Features/Navigation/RootTabView.swift`]: rootTabView(spec),
      [`${projectName}/README.md`]: generatedReadme(spec)
    }
  };
}

function swiftAppEntry(spec) {
  return `import SwiftUI

@main
struct ${spec.export.xcodeProjectName}App: App {
    var body: some Scene {
        WindowGroup {
            RootTabView()
        }
    }
}`;
}

function rootTabView(spec) {
  const tabCases = spec.navigation.tabs
    .map((tab) => `            Text("${tab.name}")\n                .tabItem { Label("${tab.name}", systemImage: "square.grid.2x2") }`)
    .join("\n\n");

  return `import SwiftUI

struct RootTabView: View {
    var body: some View {
        TabView {
${tabCases}
        }
    }
}`;
}

function generatedReadme(spec) {
  return `# ${spec.metadata.appName}\n\nGenerated by App Generator Codex pipeline.\n\n## Product\n- Value proposition: ${spec.product.valueProposition}\n- Monetization: ${spec.product.monetization.model}\n\n## Integrations\n${spec.integrations.map((integration) => `- ${integration.type}: ${integration.purpose}`).join("\n")}`;
}
